void	change_quotes(char c, int *q)
{
	if (c == '\'' && *q == 0)
		*q = 1;
	else if (c == '\'' && *q == 1)
		*q = 0;
	else if (c == '\"' && *q == 0)
		*q = 2;
	else if (c == '\"' && *q == 2)
		*q = 0;
}
